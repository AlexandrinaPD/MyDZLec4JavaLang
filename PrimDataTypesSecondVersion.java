public class PrimDataTypesSecondVersion {

    public static void main(String[] args) {


        byte myByte1 = 10;
        short myShort1 = 100;
        int myInt1 = 20_000;
        long myLong1 = 2_000_000L;
        float myFloat1 = 20_000.265256f;
        double myDouble1 = 20_000_000.64118515;
        char myChar1 = 'B';
        boolean myBoolean1 = true;

        System.out.printf("myByte1: %d\n", myByte1);
        System.out.printf("myShort1: %d\n", myShort1);
        System.out.printf("myInt1: %d\n", myInt1);
        System.out.printf("myLong1: %d\n", myLong1);
        System.out.printf("myFloat1: %f\n", myFloat1);
        System.out.printf("myDouble1: %f\n", myDouble1);
        System.out.printf("myChar1: %c\n", myChar1);
        System.out.printf("myBoolean1: %b\n", myBoolean1);
    }
}
