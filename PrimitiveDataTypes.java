public class PrimitiveDataTypes {


    public static void main(String[] args) {

        byte myByte = 10;
        short myShort = 100;
        int myInt = 20_000;
        long myLong = 2_000_000L;
        float myFloat = 20_000.265256f;
        double myDouble = 20_000_000.64118515;
        char myChar = 'B';
        boolean myBoolean = true;

        System.out.printf("myByte: %d\nmyShort: %d\nmyInt: %d\nmyLong: %d\nmyFloat: %f\nmyDouble: %f\nmyChar: %c\nmyBoolean: %b\n", myByte, myShort, myInt,myLong, myFloat, myDouble, myChar, myBoolean);


    }
}
